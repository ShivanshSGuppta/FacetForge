def build_facet_payload(category: str, group_entry: dict[str, object]) -> list[dict[str, object]]:
    """Build one category group's facet definitions."""
    group = str(group_entry["group"])
    focus = str(group_entry["focus"])
    requires_context = bool(group_entry["requires_context"])
    evidence_type = str(group_entry["evidence_type"])
    facets: list[dict[str, object]] = []
    for slug, description in group_entry["facets"]:
        facet_name = " ".join(part.capitalize() for part in slug.split("_"))
        rubric = (
            f"Assign 10 when the turn materially fails on {focus} with respect to {description.lower()} "
            f"Assign 25 when performance is weak or inconsistent. "
            f"Assign 50 when the signal is mixed or merely adequate. "
            f"Assign 75 when the turn is strong on this facet with only minor issues. "
            f"Assign 90 when the turn is exemplary on this facet."
        )
        facets.append(
            {
                "facet_id": f"{category}.{group}.{slug}",
                "facet_name": facet_name,
                "category": category,
                "description": description,
                "score_labels": list(DEFAULT_SCORE_LABELS),
                "rubric": rubric,
                "requires_context": requires_context,
                "applicable_speakers": ["assistant", "user"],
                "evidence_type": evidence_type,
                "version": "1.0.0",
            }
        )
    return facets


def main() -> None:
    """Generate the YAML registry files for every category."""
    target_dir = ensure_directory(CONFIG_DIR / "facets")
    for category, group_entries in GROUPS.items():
        facets: list[dict[str, object]] = []
        for group_entry in group_entries:
            facets.extend(build_facet_payload(category, group_entry))
        dump_yaml(target_dir / f"{category}.yaml", {"facets": facets})
        print(f"{category}: {len(facets)} facets")


if __name__ == "__main__":
    main()
